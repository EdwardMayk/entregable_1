    public void calcular(){
        
        
        int n1=Integer.parseInt((String) cbhoras.getSelectedItem());
        int n2=Integer.parseInt((String) cbsueldo.getSelectedItem());
        
        double sueldo=(n1*n2)*30;
        double descuento=((sueldo)*0.13);
        double sdescuento=sueldo-descuento;
        double total;    
            
        lblsueldoBruto.setText(String.valueOf(sueldo)) ;
        lbldescuento.setText(String.valueOf(descuento));
        lblsueldoDescuento.setText(String.valueOf(sdescuento));
        
        if (cbestado.getSelectedIndex()==1){
            double sbonificacion=(sdescuento)+sueldo*0.07;
            lblsueldoFamilia.setText(String.valueOf(sbonificacion));
        }else{
            lblsueldoFamilia.setText("No Esta Casado");
            
        }
         
        if (cbestado.getSelectedIndex()==1){
            double sbonificacion=(sdescuento)+sueldo*0.07;
            total = sbonificacion;
            lbltotal.setText(String.valueOf(total));
        }else{
            lbltotal.setText(String.valueOf(sdescuento));
            
        }
        
                 
    }

